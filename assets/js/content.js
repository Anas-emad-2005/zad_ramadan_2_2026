    /* ── Selector by day-of-year ─────────────────────────────── */
    function dayOfYear() {
        const now = new Date();
        const start = new Date(now.getFullYear(), 0, 0);
        const diff = now - start;
        const msPerDay = 1000 * 60 * 60 * 24;
        return Math.floor(diff / msPerDay);
    }

